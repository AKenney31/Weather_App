        t.setposition(-20, 10)
        t.dot(3, "blue")
        t.setposition(4, 8)
        t.dot(3, "blue")
        t.setposition(-9, 8)
        t.dot(3, "blue")
        t.setposition(-6, 0)
        t.dot(3, "blue")
        t.setposition(6, 2)
